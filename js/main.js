// ciclo for que se ejecuta del 1 al 20
console.log("Entrando al ciclo for");

for (let i = 1; i <= 20; i++) {
  console.log(i);
}
console.log("Saliendo del ciclo for");

//ciclo while que se ejecuta del 1 al 20
console.log("Entrando al ciclo while");
let i = 1;
while (i <= 20) {
  console.log(i);
  i++;
}
console.log("Saliendo del ciclo while");
